import { chromium } from "@playwright/test";

const browser = await chromium.launch({ headless: true });
const page = await browser.newPage({ viewport: { width: 1440, height: 900 }, colorScheme: "dark" });
await page.addInitScript(() => localStorage.setItem("ayetab-onboarded", JSON.stringify(true)));
await page.goto("http://127.0.0.1:3000/");
await page.getByTestId("home-screen").waitFor();
await page.getByTestId("settings-button").click();
await page.getByTestId("theme-option-dark").click();
await page.getByRole("button", { name: "Wallpaper" }).click();
await page.getByTestId("wallpaper-mesa").click();
await page.waitForTimeout(400);
await page.screenshot({ path: "/opt/cursor/artifacts/screenshots/overview-settings-mesa.png" });
await page.getByRole("button", { name: "Close settings" }).click();
await page.getByTestId("settings-panel").waitFor({ state: "hidden" });
await page.waitForTimeout(400);
await page.screenshot({ path: "/opt/cursor/artifacts/screenshots/overview-home-mesa.png" });
await page.getByRole("link", { name: "Library" }).click();
await page.getByTestId("library-page").waitFor();
await page.waitForTimeout(400);
await page.screenshot({ path: "/opt/cursor/artifacts/screenshots/overview-library.png" });
await page.getByRole("button", { name: "Encode & Decode 5" }).click();
await page.getByRole("button", { name: /Base64 Encode\/Decode Encode/ }).click();
await page.getByTestId("tool-input").fill("Hello AyeTab");
await page.waitForTimeout(800);
await page.screenshot({ path: "/opt/cursor/artifacts/screenshots/overview-base64.png" });
await page.getByRole("link", { name: "Home" }).click();
await page.getByTestId("home-screen").waitFor();
await page.keyboard.press("Control+k");
await page.getByTestId("command-palette").getByPlaceholder("Search tools...").fill("uuid");
await page.waitForTimeout(500);
await page.screenshot({ path: "/opt/cursor/artifacts/screenshots/overview-command-palette.png" });
await browser.close();
console.log("screenshots ok");
